# The “=” is an assignment operator
# used to assign the value on the right to the variable on the left.
num1 = 11
num2 = 12
str = "Darshan"

# The ‘==’ EQUAL to operator
# checks whether the two given operands are equal or not.
# If so, it returns true. Otherwise, it returns false.

print(num1 == num2)

# Arithmetic Operator:
# '**' is Exponentiation Operator
num3 = num1 ** num2
print(num3)

# '^'is XOR operator
# Sets each bit and set it to 1 if only one is 1,
# otherwise (if both are 1 or both are 0) it is set to 0:
# num1 = 3 => 1011
# num2 = 4 => 1100
#          => 0111

print(num1 ^ num2)
